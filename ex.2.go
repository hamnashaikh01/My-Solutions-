// VALUES

package main

import "fmt"

func main() {
	fmt.Println("go" + "lang")

	fmt.Println("5+8=", 5+8)

	fmt.Println("9.0/7.0=", 9.0/7.0)

	// Booleans
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
	fmt.Println(!false)

	a := "5"

	b := "9"

	fmt.Println(a < b)

	fmt.Println(a == b)

}
